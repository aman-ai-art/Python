# This code defines a function called `greeting_user` that takes a name as an argument and prints a personalized greeting message. The program welcomes the user, calls the `greeting_user` function with different names, and then thanks the user for using the program.
def greeting_user(name):
    print(f"Hello, {name}! \nHow are you doing today? \nI hope you are having a great day!")


print("Welcome to our program!")
greeting_user(name="AMAN")
greeting_user("Alice")
print("Thank you for using our program. Have a nice day!") 