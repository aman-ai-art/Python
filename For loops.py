# This code demonstrates the use of for loops in Python to iterate over different types of collections, such as strings, lists, and ranges. It prints the uppercase version of each character in a string, each letter in a list, and numbers in a specified range.
for word in 'Aman':
    print(word.upper())
print('\n')
for letter in ['A', 'B', 'C', 'D', 'E']:
    print(letter)
print('\n')
for number in range(1, 5):
    print(number)